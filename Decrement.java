class Decrement 
{
    public static void main(String[] args) 
    {
        int number = 10;
    
        System.out.println("Initial value of number: " + number);
        System.out.println("Post-decrement (number--): " + number--);
        System.out.println("Value of number after post-decrement: " + number);
        System.out.println("Pre-decrement (--number): " + --number);
        System.out.println("Value of number after pre-decrement: " + number);
    }
    
}
